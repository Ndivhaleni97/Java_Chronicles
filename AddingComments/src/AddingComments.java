public class AddingComments {

    public static void main(String [] args){
        
        
        System.out.println("* * * *" +  " * *      *");      //  printing a star
        System.out.println("*     *" +  " *  *     *");      //  My initials
        System.out.println("*     *" +  " *   *    *");      //  I am star
        System.out.println("*     *" +  " *    *   *");      // Always Shining
        System.out.println("*     *" +  " *     *  *");      // Within and Outside
        System.out.println("* * * *" +  " *      * *");      // I made this
       
    
    }
}
